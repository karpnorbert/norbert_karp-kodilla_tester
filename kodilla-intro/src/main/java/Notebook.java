public class Notebook {
    int weight;
    int price;

    public Notebook(int weight, int price) {
        this.weight = weight;
        this.price = price;
    }
    public void oldNotebook(int weight, int price) {
        this.weight = weight;
        this.price = price;
    }

    public void heavyNotebook(int weight, int price){
        this.weight = weight;
        this.price = price;
    }
}
