package com.kodilla.basic_assertion;
import java.lang.Math;

public class Calculator {
    public int sum(int a, int b){
        return a + b;
    }
    public int subtrack(int a1, int b2){
        return a1 - b2;
    }

    public double pow(double a2, double b3){
        return pow(a2, b3);
   }
}

